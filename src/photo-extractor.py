import os
import shutil

def copy_images(src_dir, dst_dir, numbers):
    # 디렉토리 내의 모든 파일을 검사
    for filename in os.listdir(src_dir):
        # 숫자 중 하나가 파일 이름에 포함되어 있다면,
        for number in numbers:
            if str(number) in filename:
                # 해당 파일을 복사할 디렉토리로 복사
                shutil.copy(os.path.join(src_dir, filename), dst_dir)
                break

